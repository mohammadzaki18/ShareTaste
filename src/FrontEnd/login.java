    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String username = uname.getText().trim();
    String pword = new String(password.getPassword()).trim();

    if (username.isEmpty() || pword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Username dan Password wajib diisi");
        return;
    }

    try {
        Connection conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/sharetaste", "root", ""
        );
        String sql = "SELECT * FROM user WHERE username = ? AND password = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, pword);

        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Login berhasil!");

            // Buka halaman home
            new home().setVisible(true);  // Ubah ke nama class sebenarnya jika beda
            this.dispose(); // Tutup halaman login
        } else {
            JOptionPane.showMessageDialog(this, "Username atau Password salah!");
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

